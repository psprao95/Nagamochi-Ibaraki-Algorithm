
public class Edge {

	private int first;
	private int second;
	public Edge(int x,int y)
	{
		this.first=x;
		this.second=y;
	}
	
	public int getFirst()
	{
		return first;
	}
	
	public int getSecond()
	{
		return second;
	}
	
	public void setFirst(int s)
	{
		this.first=s;
	}
	
	public void setSecond(int s)
	{
		this.second=s;
	}
	
}
